#include "libft.h"

int		ft_atoi(const char *str)
{
	long int			sign;
	unsigned long int	res;

	sign = 1;
	res = 0;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t' ||
				*str == '\v' || *str == '\f' || *str == '\r'))
		++str;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str && *str >= '0' && *str <= '9')
	{
		if (res > 9223372036854775807)
			return (sign == -1 ? 0 : -1);
		res = res * 10 + (long int)(*str - '0');
		str++;
	}
	return (res * sign);
}
