int		ft_sqrt(int nb)
{
	int		n;
	int		sqrt;

	n = 1;
	sqrt = 0;
	if (nb > 0)
	{
		while (n * n < nb)
			n++;
		if (nb % n == 0)
			sqrt = n;
		else
			sqrt = n - 1;
	}
	else
		return (0);
	return (sqrt);
}
